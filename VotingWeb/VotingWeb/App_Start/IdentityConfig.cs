    public class EmailService : IIdentityMessageService
    {
        private static readonly Logger logger = LogManager.GetCurrentClassLogger();

        public async Task SendAsync(IdentityMessage message)
        {
            try
            {
                // Plug in your email service here to send an email.
                using (var client = new SmtpClient(Settings.Default.SMTP_SERVER))
                {
                    client.Credentials = new NetworkCredential(Settings.Default.SMTP_ACCOUNT, Settings.Default.SMTP_PASSWORD);
                    var addressFrom = new MailAddress(Settings.Default.SMTP_SENDER);
                    var addressTo = new MailAddress(message.Destination);
                    var mailMessage = new MailMessage(addressFrom, addressTo)
                    {
                        Subject = message.Subject,
                        SubjectEncoding = System.Text.Encoding.UTF8,
                        Body = message.Body,
                        BodyEncoding = System.Text.Encoding.UTF8,
                        IsBodyHtml = true,
                    };
                    await client.SendMailAsync(mailMessage);
                }
            }
            catch (Exception exception)
            {
                logger.Error(exception, "Error sending email");
            }
        }
    }

